namespace CML.CommonEx.NetworkEx
{
    /// <summary>
    /// 传输速率单位
    /// </summary>
    public enum ESpeedUnit
    {
        /// <summary>
        /// 字节
        /// </summary>
        Byte = 0,
        /// <summary>
        /// 千字节（2^10 Byte）
        /// </summary>
        KB = 10,
        /// <summary>
        /// 兆字节（2^20 Byte）
        /// </summary>
        MB = 20,
        /// <summary>
        /// 吉字节（2^30 Byte）
        /// </summary>
        GB = 30,
        /// <summary>
        /// 太字节（2^40 Byte）
        /// </summary>
        TB = 40,
    }
}
